        n.setNotaAV1(Double.parseDouble(txtAV1.getText()));
        n.setNotaAV2(Double.parseDouble(txtAV2.getText()));
        n.setNotaAV3(Double.parseDouble(txtAV3.getText()));
        n.calcularMedia(n.getNotaAV1(), n.getNotaAV2(), n.getNotaAV3());
        
        n.adicionarMedia(n);
        preencherTabela();
        JOptionPane.showMessageDialog(null, "Nota cadastrada.");

    public void preencherTabela(){
        DefaultTableModel tabela = (DefaultTableModel) tblMedias.getModel();
        tabela.setNumRows(0);
        
        for (int i = 0; i < Notas.medias.size(); i++) {
            tabela.addRow(new Object[]{
                Notas.medias.get(i).getMedia()
            });
        }
    }
    
